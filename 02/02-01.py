month = input("Введите месяц (июль или август): ")

if month == "июль":
    print("Маршрут подходит. Женя собирается посетить Тулу.")
elif month == "август":
    print("Маршрут подходит. Женя собирается посетить Пензу.")
else:
    print("Маршрут не подходит. Женя должна выбрать месяц июль или август.")